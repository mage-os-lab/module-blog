<?php

declare(strict_types=1);

namespace MageOS\Blog\Plugin\Repository\Tag;

use Magento\UrlRewrite\Model\OptionProvider;
use Magento\UrlRewrite\Model\UrlFinderInterface;
use Magento\UrlRewrite\Model\UrlPersistInterface;
use Magento\UrlRewrite\Service\V1\Data\UrlRewrite;
use Magento\UrlRewrite\Service\V1\Data\UrlRewriteFactory;
use MageOS\Blog\Api\Data\TagInterface;
use MageOS\Blog\Api\TagRepositoryInterface;
use MageOS\Blog\Model\Url\UrlRewriteBuilder;

class UrlRewritePlugin
{
    public function __construct(
        private readonly UrlRewriteBuilder $urlRewriteBuilder,
        private readonly UrlPersistInterface $urlPersist,
        private readonly UrlFinderInterface $urlFinder,
        private readonly UrlRewriteFactory $urlRewriteFactory,
    ) {
    }

    public function afterSave(TagRepositoryInterface $subject, TagInterface $result): TagInterface
    {
        $storeIds = $result->getStoreIds();
        if ($storeIds === []) {
            return $result;
        }

        $tagId = (int) $result->getTagId();
        $newRows = $this->urlRewriteBuilder->buildForTag($result, $storeIds);
        $newRequestPath = 'blog/tag/' . $result->getUrlKey();
        $newTargetPath = 'blog/tag/view/id/' . $tagId;

        $redirectRows = $this->buildRedirectsForChangedSlug(
            $tagId,
            $newRequestPath,
            $newTargetPath,
        );

        $this->urlPersist->replace(array_merge($redirectRows, $newRows));

        return $result;
    }

    public function afterDelete(TagRepositoryInterface $subject, bool $result, TagInterface $tag): bool
    {
        $this->urlPersist->deleteByData([
            UrlRewrite::ENTITY_TYPE => UrlRewriteBuilder::ENTITY_TYPE_TAG,
            UrlRewrite::ENTITY_ID => (int) $tag->getTagId(),
        ]);

        return $result;
    }

    public function afterDeleteById(TagRepositoryInterface $subject, bool $result, int $id): bool
    {
        $this->urlPersist->deleteByData([
            UrlRewrite::ENTITY_TYPE => UrlRewriteBuilder::ENTITY_TYPE_TAG,
            UrlRewrite::ENTITY_ID => $id,
        ]);

        return $result;
    }

    /**
     * @return UrlRewrite[]
     */
    private function buildRedirectsForChangedSlug(
        int $tagId,
        string $newRequestPath,
        string $newTargetPath,
    ): array {
        $existing = $this->urlFinder->findAllByData([
            UrlRewrite::ENTITY_TYPE => UrlRewriteBuilder::ENTITY_TYPE_TAG,
            UrlRewrite::ENTITY_ID => $tagId,
            UrlRewrite::REDIRECT_TYPE => 0,
        ]);

        $redirects = [];
        $seen = [];
        foreach ($existing as $row) {
            $oldPath = (string) $row->getRequestPath();
            if ($oldPath === '' || $oldPath === $newRequestPath) {
                continue;
            }
            $storeId = (int) $row->getStoreId();
            $key = $storeId . '|' . $oldPath;
            if (isset($seen[$key])) {
                continue;
            }
            $seen[$key] = true;

            $redirect = $this->urlRewriteFactory->create();
            $redirect->setEntityType(UrlRewriteBuilder::ENTITY_TYPE_TAG)
                ->setEntityId($tagId)
                ->setRequestPath($oldPath)
                ->setTargetPath($newTargetPath)
                ->setIsAutogenerated(0)
                ->setStoreId($storeId)
                ->setRedirectType(OptionProvider::PERMANENT);
            $redirects[] = $redirect;
        }

        return $redirects;
    }
}
